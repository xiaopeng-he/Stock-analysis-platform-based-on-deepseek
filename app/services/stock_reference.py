def search_popular(keyword: str, limit: int = 15) -> list[dict]:
    """在热门股票列表中搜索。"""
    results = []
    kw = keyword.lower()
    for code, name in POPULAR_STOCKS:
        if kw in code or kw in name.lower():
            results.append({"code": code, "name": name, "price": 0, "change_pct": 0})
        if len(results) >= limit:
            break
    return results
